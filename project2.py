def circle_move(phi, time,delay,x1,y1):
    if delay<time:
      x=0
      y=0
    else:
      if t >= 90:
        a = 0.0001 * b * 70 * t
      else:
        a = 0.00001 * t
      vy0 = 0.02 * t
      y0 = vy0 * (time-delay)
      alpha = a * (np.pi / 180) * (time-delay)
      R = alpha * (time-delay)
      x=R*np.cos(phi) + x1
      y=y0+R*np.sin(phi) + y1
    return x, y

